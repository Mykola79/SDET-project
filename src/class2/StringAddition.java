package class2;

public class StringAddition {
    public static void main(String[] args) {
        String firstName="George";
        String lastName="Michael";

        System.out.println(firstName+lastName);
        System.out.println(firstName+" "+lastName);
        System.out.println("*****"+firstName+" "+lastName+"******");
        System.out.println(firstName+" lastName");
        System.out.println("10+10");

        String space="";
        System.out.println(firstName+space);
        //I_home_the_street

        String verb="go";
        String adjective1="across";
        String adjective2="empty";

        System.out.println("I "+verb+" home "+adjective1+" the "+adjective2+" street");
    }
}
